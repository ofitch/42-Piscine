#include <unistd.h>

int		main(void)
{
	int low;
	int up;

	low = 'a';
	up = 'A';
	while (low <= 'z' ||  up <= 'Z')
	{
		if (up % 2 == 0)
		{
			write(1, &up, 1);
		}
		else
		{
			write(1, &low, 1);
		}
		low++;
		up++;
	}
	write(1, "\n", 1);
}
